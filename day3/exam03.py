import pandas as pd

# ✅ 데이터프레임 생성 (가상의 사람 정보)
df = pd.DataFrame({
    '이름': ['홍길동', '김철수', '이영희'],   # 이름은 문자열
    '나이': ['25', '30', '28'],             # 나이가 문자열로 되어 있음
    '키': [170, 180, 300]                   # 키 값에 이상치(300) 포함
})

# ✅ 나이 타입 변환: 문자열 → 정수형으로 바꾸기
# 숫자로 연산하려면 반드시 정수형(int)이어야 함
df['나이'] = df['나이'].astype(int)

# ✅ 이상치 제거: 현실적으로 있을 수 없는 키 값(300)을 걸러내기
# 조건: 키가 100 이상, 200 이하인 데이터만 남긴다
df = df[(df['키'] >= 100) & (df['키'] <= 200)]

# ✅ 결과 출력
print(df)
